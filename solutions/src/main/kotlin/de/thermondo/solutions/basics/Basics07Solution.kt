package de.thermondo.solutions.basics

/**
 * You can create enum classes by using the enum keyword followed by class,
 * an enum has its own specialized type, indicating that something has a certain number of possible values.
 *
 * Let's create an enum class named Basics07Solution that specifies the days of the week (Monday - Sunday).
 * Enum values are written in all caps, ex: MONDAY
 */

enum class Basics07Solution {
    SUNDAY,
    MONDAY,
    TUESDAY,
    WEDNESDAY,
    THURSDAY,
    FRIDAY,
    SATURDAY
}
